namespace BidMessage
{
    /// <summary>
    /// Enumeration <c>AuctionSessions</c> represents all supported sessions of quote messages , from A to H, plus a reserved undefined value.
    /// </summary>
    public enum AuctionSessions
    {
        Undefined = 'Z',
        SessionA = 'A',
        SessionB = 'B',
        SessionC = 'C',
        SessionD = 'D',
        SessionE = 'E',
        SessionF = 'F',
        SessionG = 'G',
        SessionH = 'H',
    }
}
